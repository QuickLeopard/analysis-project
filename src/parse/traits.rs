/// Базовый трейт парсера.
///
/// Парсер принимает входную строку и возвращает:
/// - остаток строки, который ещё не был разобран;
/// - распарсенное значение `Dest`.
///
/// Ошибка `Err(())` означает, что текущий парсер не смог распознать вход.
pub trait Parser {
    type Dest;
    fn parse<'a>(&self, input: &'a str) -> Result<(&'a str, Self::Dest), ()>;
}
/// Трейт типов, которые могут построить свой parser-комбинатор.
///
/// Это упрощённый аналог `serde::Deserialize`: тип описывает,
/// как собрать парсер самого себя из базовых примитивов и комбинаторов.
pub trait Parsable: Sized {
    type Parser: Parser<Dest = Self>;
    fn parser() -> Self::Parser;
}
